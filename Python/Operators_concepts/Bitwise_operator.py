#--------------------------------
#Bitwise - &,|,^,~,<<,>>
#--------------------------------


a=4
b=10

# Bitwise and &

print("Bitwise & : {}".format(a&b))

#Bitwise or |

print("Bitwise | : {}".format(a|b))

#Bitwise X-or ^

print("Bitwise ^ : {}".format(a^b))

#Bitwise Complement ~

#Positive value

print("Bitwise complement positive value~ :{}".format(~6))

#Negative value

print("Bitwise complement negative value~ :{}".format(~-7))

#Bitwise left shift <<

print("Bitwise left shift << : {}".format(a<<2))

#Bitwise right shift >>

print("Bitwise right shift >> : {}".format(a>>2))
