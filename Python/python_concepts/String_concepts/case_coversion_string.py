#---------------------------------------
# Case conversion String
#---------------------------------------

string="Vignesh mahendran"

#list of string functions:

print(f"list of functions: {dir(string)}")

#Upper()

print(f"Convert to Uppercase: {string.upper()}")

#lower()

print(f"Convert to Lowercase: {string.lower()}")

#title()

print(f"Convert to title: {string.title()}")

#swapcase()

print(f"Convert using Swapcase: {string.swapcase()}")

#Capitalize

print(f"Convert using Capitalize: {string.capitalize()}")

#casefold

print(f"Convert using Casefold: {string.casefold()}")
