"""
Special Characters

- This is used to use special character inside string
- \n-newline - It is used to take string to newline
- \b-back space - It is used to move the string one position backwards
- \t- new tab - It is used to move the string one tab
- \ - escape symbol - It is used to have special characters like ',",/ (windows path) inside string

"""

print("I am Vigneshwaran.I am an Engineer")

# newline:

print("I am Vigneshwaran.\nI am an Engineer")

# backspace:

print("I am Vigneshwaran.\bI am an Engineer")

# newtab:

print("I am Vigneshwaran.\tI am an Engineer")

#Escape symbol:

#print("c:\Windows\System"32")

print("c:\Windows\System'32")

print("c:\Windows\System\"32")


