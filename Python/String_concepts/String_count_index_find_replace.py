# Count(),index(),find(),replace() string function
#--------------------------------------------


string="python is the general purpose programing language"

#count()

print(f"String count of character: {string.count('p')}")


#index()

print(f"String index value of character: {string.index('p')}")

#find()

print(f"String find value of character: {string.find('p')}")


#replace()

print(f"String replace substring value of character: {string.replace('p','v')}")

#index('char',index_value)

print(f"String index value of character: {string.index('p',2)}")

#find('char',index_value)

print(f"String find value of character: {string.find('p',26)}")

#count('char',indexvalue)

print(f"String count of character: {string.count('p',2)}")

